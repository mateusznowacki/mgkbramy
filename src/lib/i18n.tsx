type Language = 'pl' | 'de';

interface I18nContextProps {
  language: Language;
  setLanguage: (lang: Language) => void;
  t: (section: string, key: string) => string;
}

const I18nContext = createContext<I18nContextProps | undefined>(undefined);

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  let defaultLang: Language = 'de';
  if (typeof navigator !== 'undefined' && navigator.language && navigator.language.startsWith('pl')) {
    defaultLang = 'pl';
  }
  const [language, setLanguage] = useState<Language>(defaultLang);

  const t = (section: string, key: string) => {
    return (
      translations[language]?.[section]?.[key] ||
      translations['pl']?.[section]?.[key] ||
      key
    );
  };

  return (
    <I18nContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </I18nContext.Provider>
  );
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used within an I18nProvider');
  }
  return context;
}; 